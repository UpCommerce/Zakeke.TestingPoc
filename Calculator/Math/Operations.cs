namespace Calculator.Math;
public static class Operations
{
    public static int AddTwoNumbers(int a, int b) => a + b;
    public static int SubtractTwoNumbers(int a, int b) => a - b;
    public static int MultiplyTwoNumbers(int a, int b) => a * b;
    public static int DivideTwoNumbers(int a, int b) => a / b;

}